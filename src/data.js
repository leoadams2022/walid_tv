const extractFunction_3 = (element, baseUrl = "https://www.elahmad.com") => {
  const channels = [];
  let currentServerName = null;

  // Get all direct children of the scrol element
  const children = element.children;

  for (let i = 0; i < children.length; i++) {
    const child = children[i];

    // Check if it's a server header (h2 with class "countrytv")
    if (child.tagName === "H2" && child.classList.contains("countrytv")) {
      // Extract server name from the h2 text
      currentServerName = child.textContent.trim();
    }
    // Check if it's a channel div
    else if (child.tagName === "DIV" && child.children.length > 0) {
      const anchor = child.querySelector("a");
      if (anchor) {
        // Extract channel name from anchor text
        let channelName = anchor.textContent.trim();
        channelName = channelName.replace(/\s+/g, " ").trim();

        // Extract icon URL from img tag
        let iconUrl = null;
        const img = anchor.querySelector("img");
        if (img) {
          const src = img.getAttribute("src");
          if (src) {
            // Handle relative URLs
            if (src.startsWith("/")) {
              iconUrl = baseUrl + src;
            } else if (!src.startsWith("http")) {
              iconUrl = baseUrl + "/" + src;
            } else {
              iconUrl = src;
            }
          }
        }

        // Extract channel URL
        let channelUrl = null;
        const onclick = anchor.getAttribute("onclick");

        if (onclick) {
          // Try to extract URL from openCentered function call
          let urlMatch = onclick.match(/openCentered\(['"]([^'"]+)['"]/);
          if (!urlMatch) {
            // Try to extract URL from iframe function call
            urlMatch = onclick.match(/iframe\(['"]([^'"]+)['"]/);
          }
          if (urlMatch) {
            channelUrl = urlMatch[1];
          }
        }

        // If no URL found in onclick, check href
        if (!channelUrl) {
          const href = anchor.getAttribute("href");
          if (
            window.location.href ===
            "https://www.elahmad.com/tv/live-arabic-channels.php"
          ) {
            const urlKey = parseInt(href.match(/\d+/)[0]);
            // surl is a global variable defined in the page
            // eslint-disable-next-line no-undef
            channelUrl = surl[urlKey];
            console.log(channelUrl);
          } else if (href && href !== "javascript:void(0)") {
            channelUrl = href;
          }
        }

        // Process the URL if found
        if (channelUrl) {
          // Handle relative URLs
          if (channelUrl.startsWith("/")) {
            channelUrl = baseUrl + channelUrl;
          } else if (!channelUrl.startsWith("http")) {
            channelUrl = baseUrl + "/" + channelUrl;
          }

          // Only add if we have a valid channel name and URL
          if (channelName && channelUrl) {
            channels.push({
              name: channelName,
              url: channelUrl,
              iconUrl: iconUrl || null,
              groupName: currentServerName || "Uncategorized",
            });
          }
        }
      }
    }
  }

  // Remove duplicates based on URL
  const uniqueChannels = [];
  const urlSet = new Set();

  for (const channel of channels) {
    if (!urlSet.has(channel.url)) {
      urlSet.add(channel.url);
      uniqueChannels.push(channel);
    }
  }

  return uniqueChannels;
};
